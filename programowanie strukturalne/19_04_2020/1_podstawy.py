print(type(y))
print(y)

surname = "Kowalski"
print(surname[0])       # K
print(surname[0:3])     # Kow
print(surname[-2])      # k
print(surname[-2:])     # ki
print(surname[:-2])     # Kowals
print(surname[:-2:2])   # Kwl
